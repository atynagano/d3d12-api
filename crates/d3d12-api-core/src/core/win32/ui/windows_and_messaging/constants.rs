impl BitOr for WindowMessage {
	type Output = WindowMessage;
	fn bitor(self, rhs: Self) -> Self::Output {
		unsafe { transmute(self as u32 | rhs as u32) }
	}
}

impl BitOrAssign for WindowMessage {
	fn bitor_assign(&mut self, rhs: Self) {
		*self = *self | rhs;
	}
}

impl WindowMessage {
    pub fn contains(self, other: WindowMessage) -> bool {
        (self as u32) & (other as u32) == (other as u32)
    }
}

